# RAND() -> NUMERO ALEATORIO ENTRE 0 Y 1

SELECT TRUNCATE(RAND()*10+1, 0);
SELECT CEIL(RAND()*10);

# ROUND(numero, decimales) -> REDONDEAR
# TRUNCATE(numero, decimales) -> TRUNCA
# FLOOR(numero) -> ENTERO INFERIOR SON MUY PARECIDOS
# CEIL(numero) -> ENTERO SUPERIOR

SELECT ROUND(7.896599568, 2);
SELECT TRUNCATE(7.896599568, 1);
SELECT FLOOR(7.896599568);
SELECT CEIL(7.1);
